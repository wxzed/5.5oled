/**********************************************************
* 函数功能 ---> IIC启动
* 入口参数 ---> none
* 返回数值 ---> none
* 功能说明 ---> SCL高电平期间，SDA由高电平突变到低电平时启动总线
**********************************************************/
void IIC_Start(void)
{
	IIC_SDA_OUT();	//设置成输出
	
	IIC_SDA = 1;	//为SDA下降启动做准备
	IIC_SCL = 1;	//在SCL高电平时，SDA为下降沿时候总线启动
	IIC_Delay();
	IIC_SDA = 0;	//突变，总线启动
	IIC_Delay();
	IIC_SCL = 0;
	IIC_Delay();
}
/**********************************************************
* 函数功能 ---> IIC停止
* 入口参数 ---> none
* 返回数值 ---> none
* 功能说明 ---> SCL高电平期间，SDA由低电平突变到高电平时停止总线
**********************************************************/
void IIC_Stop(void)
{
	IIC_SDA_OUT();	//设置成输出

	IIC_SDA = 0;	//为SDA上升做准备
	IIC_Delay();
	IIC_SCL = 1;	//在SCL高电平时，SDA为上升沿时候总线停止
	IIC_Delay();
	IIC_SDA = 1;	//突变，总线停止
	IIC_Delay();
}
/**********************************************************
* 函数功能 ---> 主机向从机发送应答信号
* 入口参数 ---> a：应答信号
*                  0：应答信号
*                  1：非应答信号
* 返回数值 ---> none
* 功能说明 ---> none
**********************************************************/
void IIC_Ack(u8 a)
{
	IIC_SDA_OUT();	//设置成输出

	if(a)	IIC_SDA = 1;	//放上应答信号电平
	else	IIC_SDA = 0;

	IIC_Delay();
	IIC_SCL = 1;	//为SCL下降做准备
	IIC_Delay();
	IIC_SCL = 0;	//突变，将应答信号发送过去
	IIC_Delay();
}
/**********************************************************
* 函数功能 ---> 向IIC总线发送一个字节数据
* 入口参数 ---> dat：要发送的数据
* 返回数值 ---> ack：返回应答信号
* 功能说明 ---> none
**********************************************************/
u8 IIC_Write_Byte(u8 dat)
{
	u8 i;
	u8 iic_ack=0;	//iic应答标志

	IIC_SDA_OUT();	//设置成输出

	for(i = 0;i < 8;i++)
	{
		if(dat & 0x80)	IIC_SDA = 1;	//判断发送位，先发送高位
		else	IIC_SDA = 0;
			
		IIC_Delay();
		IIC_SCL = 1;	//为SCL下降做准备
		IIC_Delay();
		IIC_SCL = 0;	//突变，将数据位发送过去
		dat<<=1;	//数据左移一位
	}	//字节发送完成，开始接收应答信号

	IIC_SDA = 1;	//释放数据线

	IIC_SDA_IN();	//设置成输入

	IIC_Delay();
	IIC_SCL = 1;	//为SCL下降做准备
	IIC_Delay();
	iic_ack |= IN_SDA;	//读入应答位
	IIC_SCL = 0;
	return iic_ack;	//返回应答信号
}
/**********************************************************
* 函数功能 ---> 从IIC总线上读取一个字节数据
* 入口参数 ---> none
* 返回数值 ---> x：读取到的数据
* 功能说明 ---> none
**********************************************************/	
u8 IIC_Read_Byte(void)
{
	u8 i;
	u8 x=0;

	IIC_SDA = 1;	//首先置数据线为高电平

	IIC_SDA_IN();	//设置成输入

	for(i = 0;i < 8;i++)
	{
		x <<= 1;	//读入数据，高位在前

		IIC_Delay();
		IIC_SCL = 1;	//突变
		IIC_Delay();
		
		if(IN_SDA)	x |= 0x01;	//收到高电平

		IIC_SCL = 0;
		IIC_Delay();
	}	//数据接收完成
	IIC_SCL = 0;

	return x;	//返回读取到的数据
}


#include "iic.h"

/**********************************************************
* 函数功能 ---> GPIO初始化
* 入口参数 ---> none
* 返回数值 ---> none
* 功能说明 ---> STM32平台，其他平台请修改或者注释掉
**********************************************************/
void IIC_GPIO_Init(void)
{	
	GPIO_InitTypeDef GPIO_InitStructure;
	
	RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOB, ENABLE);	//开启GPIOB外设时钟，PB.6（SCL） and PB.7（SDA）
	
	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_6 | GPIO_Pin_7;	//初始化GPIOB.6 and GPIOB.7
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;	//GPIO翻转速度为50MHz

	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP;	//设置为推挽输出

	GPIO_Init(GPIOB, &GPIO_InitStructure);	//初始化GPIO相关结构体

	GPIO_SetBits(GPIOB, GPIO_Pin_6 | GPIO_Pin_7);	//输出高电平，置IIC总线空闲	
}

